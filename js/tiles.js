function createSprite(map, palette, background) {
  var canvas = document.createElement('canvas');
  var context = canvas.getContext('2d');

  canvas.width = 25;
  canvas.height = 25;

    // context.scale(1*(tileSize/25),1*(tileSize/25));
	 if(background != undefined){
	     context.fillStyle = goodPalette[5];
	      context.strokeStyle = context.fillStyle;
		  var h = canvas.height * 2;
		  var w = canvas.width * 2;
  
	      context.fillRect(0,0,w,h);
  
  
	     var passes = 80*(tileSize/25);
	     while (passes--) {
	       context.fillStyle = goodPalette[6];
	       context.strokeStyle = context.fillStyle;

	       context.fillRect(Math.random() * w - 2, Math.random() * h - 2,  1*(tileSize/25),1*(tileSize/25));

	     }
	 }

  
  for (var y = 0; y < map.length; y++) {
      for (var x = 0; x < map[y].length; x++) {
			 var pixel = map[y][x];
			 if (pixel != undefined && pixel != 0){
			      context.fillStyle = palette[pixel-1]; 
			      context.strokeStyle = context.fillStyle;
				  context.fillRect(x,y,1,1);
			 }
      }
  }

  var material = new Image();
  material.src = canvas.toDataURL();
  return material;
}

